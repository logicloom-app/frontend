"use client";

import { useGetWebsitePackages } from "@/lib/hooks/useAdmin";
import EditWebsitePackage from "./_components/EditWebsitePackage";
import { formatDate } from "@/lib/utils/utils";
import BlurFade from "@/components/ui/blur-fade";
import { Package, TrendingUp, Sparkles, Calendar, Euro } from "lucide-react";

export default function WebsitePackages() {
  const { data, isLoading } = useGetWebsitePackages();
  const { packages } = data || {};

  if (isLoading) {
    return (
      <div className="w-full h-full px-4 py-6 overflow-y-scroll max-h-[calc(100vh-6rem)]">
        <div className="max-w-7xl mx-auto">
          <div className="flex items-center justify-center py-20">
            <div className="animate-spin rounded-full h-12 w-12 border-b-2 border-cyan-600"></div>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="w-full h-full px-4 py-6 overflow-y-scroll max-h-[calc(100vh-6rem)]">
      <div className="max-w-7xl mx-auto space-y-6">
        {/* Header with Gradient */}
        <BlurFade delay={0.1}>
          <div className="bg-gray-50 dark:bg-gray-800/60 backdrop-blur-xl border-2 border-gray-200/60 dark:border-gray-700/50 rounded-3xl p-8 shadow-2xl">
            <div className="flex flex-col sm:flex-row justify-between items-start sm:items-center gap-4">
              <div>
                <h1 className="text-4xl font-bold bg-gradient-to-r from-blue-600 via-purple-600 to-emerald-600 bg-clip-text text-transparent mb-2">
                  Website Packages
                </h1>
                <p className="text-gray-600 dark:text-gray-400 flex items-center gap-2">
                  <Package className="w-4 h-4" />
                  Configure website pricing packages
                </p>
              </div>
              <div className="flex items-center gap-2 px-4 py-2 bg-gradient-to-r from-blue-500/10 to-emerald-500/10 border border-blue-500/20 rounded-xl">
                <Sparkles className="w-5 h-5 text-blue-600 dark:text-blue-400" />
                <span className="font-semibold text-sm">
                  {packages?.length || 0} Package{packages?.length !== 1 ? "s" : ""}
                </span>
              </div>
            </div>
          </div>
        </BlurFade>

        {/* Packages Grid */}
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-6">
          {packages?.map((pkg, index) => (
            <BlurFade key={pkg?.id} delay={0.2 + index * 0.1}>
              <div className="group bg-gray-50 dark:bg-gray-800/60 backdrop-blur-xl border-2 border-gray-200/60 dark:border-gray-700/50 rounded-2xl p-6 shadow-xl hover:shadow-2xl hover:shadow-blue-500/20 transition-all duration-500 hover:-translate-y-1">
                {/* Gradient Overlay */}
                <div className="absolute inset-0 bg-gradient-to-br from-blue-500/0 via-purple-500/0 to-emerald-500/0 group-hover:from-blue-500/5 group-hover:via-purple-500/5 group-hover:to-emerald-500/5 rounded-2xl transition-all duration-500"></div>

                {/* Header */}
                <div className="relative flex items-center justify-between mb-4">
                  <div className="p-3 bg-gradient-to-br from-blue-500 via-purple-500 to-emerald-500 rounded-xl shadow-lg group-hover:scale-110 transition-transform duration-300">
                    <Package className="w-6 h-6 text-white animate-pulse" />
                  </div>
                  <div className="flex flex-col items-end gap-1">
                    <span className="px-3 py-1 bg-gray-100 dark:bg-gray-800 rounded-full text-xs font-semibold text-gray-600 dark:text-gray-400">
                      ID: {pkg?.id}
                    </span>
                    {pkg?.badge && (
                      <span className="px-3 py-1 bg-gradient-to-r from-purple-600 to-indigo-600 text-white rounded-full text-xs font-semibold">
                        {pkg.badge}
                      </span>
                    )}
                  </div>
                </div>

                {/* Package Name */}
                <h3 className="relative text-xl font-bold text-gray-900 dark:text-white mb-2">
                  {pkg?.name}
                </h3>

                {/* Description */}
                <p className="relative text-sm text-gray-600 dark:text-gray-400 mb-4 line-clamp-2">
                  {pkg?.description}
                </p>

                {/* Pricing */}
                <div className="relative mb-4">
                  <div className="flex items-baseline gap-2 mb-2">
                    {pkg?.original_price && (
                      <span className="text-sm text-gray-500 dark:text-gray-400 line-through">
                        €{pkg.original_price?.toLocaleString()}
                      </span>
                    )}
                    <span className="text-2xl font-bold bg-gradient-to-r from-blue-600 via-purple-600 to-emerald-600 bg-clip-text text-transparent">
                      €{pkg?.discounted_price?.toLocaleString()}
                    </span>
                  </div>
                </div>

                {/* Features Count */}
                <div className="relative mb-4 text-sm text-gray-600 dark:text-gray-400">
                  <span className="flex items-center gap-2">
                    <TrendingUp className="w-4 h-4" />
                    {pkg?.features?.length || 0} Feature
                    {pkg?.features?.length !== 1 ? "s" : ""}
                  </span>
                </div>

                {/* Slug */}
                <div className="relative mb-4">
                  <span className="text-xs text-gray-500 dark:text-gray-400 bg-gray-100 dark:bg-gray-800 px-2 py-1 rounded">
                    /{pkg?.slug}
                  </span>
                </div>

                {/* Timestamps */}
                <div className="relative space-y-2 mb-4 text-xs text-gray-600 dark:text-gray-400">
                  <div className="flex items-center gap-2">
                    <Calendar className="w-3 h-3" />
                    <span>Created: {formatDate(pkg?.created_at)}</span>
                  </div>
                  <div className="flex items-center gap-2">
                    <Calendar className="w-3 h-3" />
                    <span>Updated: {formatDate(pkg?.updated_at)}</span>
                  </div>
                </div>

                {/* Edit Button */}
                <div className="relative">
                  <EditWebsitePackage pkg={pkg} />
                </div>
              </div>
            </BlurFade>
          ))}
        </div>

        {/* Empty State */}
        {packages?.length === 0 && (
          <BlurFade delay={0.2}>
            <div className="bg-gray-50 dark:bg-gray-800/60 backdrop-blur-xl border-2 border-gray-200/60 dark:border-gray-700/50 rounded-2xl p-12 text-center">
              <Package className="w-16 h-16 mx-auto mb-4 text-gray-400" />
              <p className="text-gray-600 dark:text-gray-400 text-lg">
                No website packages yet. Create your first package!
              </p>
            </div>
          </BlurFade>
        )}
      </div>
    </div>
  );
}
